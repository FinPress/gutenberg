/**
 * Generates lib-data.ts with Base64-encoded VIPS library files
 */

const fs = require( 'fs' );
const path = require( 'path' );

function generateLibData() {
	const libDir = path.join( __dirname, 'lib' );
	const outputFile = path.join( __dirname, 'src', 'lib-data.ts' );

	// Ensure lib directory exists
	if ( ! fs.existsSync( libDir ) ) {
		fs.mkdirSync( libDir, { recursive: true } );
	}

	// Find wasm-vips module in node_modules (check both local and root)
	const possiblePaths = [
		path.join( __dirname, 'node_modules', 'wasm-vips', 'lib' ),
		path.join( __dirname, '..', '..', 'node_modules', 'wasm-vips', 'lib' ),
	];

	let wasmVipsLibPath = null;
	for ( const possiblePath of possiblePaths ) {
		if ( fs.existsSync( possiblePath ) ) {
			wasmVipsLibPath = possiblePath;
			break;
		}
	}

	if ( ! wasmVipsLibPath ) {
		console.error(
			'wasm-vips module not found in node_modules. Please run npm install.'
		);
		process.exit( 1 );
	}

	const sourceVipsWasmPath = path.join( wasmVipsLibPath, 'vips.wasm' );
	const sourceVipsJsPath = path.join( wasmVipsLibPath, 'vips.js' );
	const targetVipsWasmPath = path.join( libDir, 'vips.wasm' );
	const targetVipsJsPath = path.join( libDir, 'vips.js' );

	// Copy files from node_modules to lib directory
	if (
		! fs.existsSync( sourceVipsWasmPath ) ||
		! fs.existsSync( sourceVipsJsPath )
	) {
		console.error( 'VIPS files not found in wasm-vips module.' );
		process.exit( 1 );
	}

	console.log( `Copying VIPS files from ${ wasmVipsLibPath }...` );
	fs.copyFileSync( sourceVipsWasmPath, targetVipsWasmPath );
	fs.copyFileSync( sourceVipsJsPath, targetVipsJsPath );

	const vipsWasmPath = targetVipsWasmPath;
	const vipsJsPath = targetVipsJsPath;

	// Read files and convert to Base64
	const vipsWasmBuffer = fs.readFileSync( vipsWasmPath );
	const vipsJsBuffer = fs.readFileSync( vipsJsPath );

	const vipsWasmBase64 = vipsWasmBuffer.toString( 'base64' );
	const vipsJsBase64 = vipsJsBuffer.toString( 'base64' );

	// Create data URLs
	const vipsWasmDataUrl = `data:application/wasm;base64,${ vipsWasmBase64 }`;
	const vipsJsDataUrl = `data:application/javascript;base64,${ vipsJsBase64 }`;

	// Generate TypeScript file
	const tsContent = `/**
 * Generated file containing Base64-encoded VIPS library files
 * This file is auto-generated by generate-lib-data.js
 */

export const vipsWasmDataUrl = ${ JSON.stringify( vipsWasmDataUrl ) };
export const vipsJsDataUrl = ${ JSON.stringify( vipsJsDataUrl ) };
`;

	fs.writeFileSync( outputFile, tsContent );

	console.log(
		`Generated ${ outputFile } with Base64-encoded VIPS library files`
	);
	console.log(
		`WASM size: ${ Math.round( vipsWasmBase64.length / 1024 ) }KB (Base64)`
	);
	console.log(
		`JS size: ${ Math.round( vipsJsBase64.length / 1024 ) }KB (Base64)`
	);
}

generateLibData();
